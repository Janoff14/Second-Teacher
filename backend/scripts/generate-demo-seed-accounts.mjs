const reservedEmails = new Set([teacherEmail]);

function partitionStudentsAcrossSections(total, parts) {
  if (parts <= 0) return [];
  const base = Math.floor(total / parts);
  const rem = total % parts;
  return Array.from({ length: parts }, (_, i) => base + (i < rem ? 1 : 0));
}

function sectionForStudentIndex1Based(i) {
  const counts = partitionStudentsAcrossSections(studentCount, sectionCount);
  let acc = 0;
  for (let s = 0; s < counts.length; s++) {
    acc += counts[s];
    if (i <= acc) return sectionNames[s];
  }
  return sectionNames[sectionNames.length - 1];
}

function slugifyIdentity(input) {
  return input.toLowerCase().replace(/[^a-z0-9]+/g, ".").replace(/^\.+|\.+$/g, "");
}

function studentIdentity(index1Based) {
  let zeroBased = index1Based - 1;
  while (true) {
    const first = firstNames[zeroBased % firstNames.length];
    const last = lastNames[zeroBased % lastNames.length];
    const displayName = `${first} ${last}_demo`;
    const email = `${slugifyIdentity(`${first}.${last}`)}_demo@secondteacher.dev`;
    if (!reservedEmails.has(email)) {
      return { email, displayName };
    }
    zeroBased += 1;
  }
}

