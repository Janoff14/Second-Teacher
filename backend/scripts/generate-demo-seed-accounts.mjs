/**
 * Writes docs/demo-seed-accounts.md and docs/demo-seed-accounts.csv.
 * Run from repo root: node backend/scripts/generate-demo-seed-accounts.mjs
 *
 * Keep counts and emails aligned with src/seed/demoDataset.ts.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..", "..");

const pwd = "DemoSeed2026!";
const teacherEmail = "demo.seed.teacher@secondteacher.dev";
const teacherName = "Seed Demo Teacher";
const sectionNames = ["Period A (SEED)", "Period B (SEED)", "Period C (SEED)", "Period D (SEED)"];
const studentCount = 120;
const sectionCount = sectionNames.length;

function partitionStudentsAcrossSections(total, parts) {
  if (parts <= 0) return [];
  const base = Math.floor(total / parts);
  const rem = total % parts;
  return Array.from({ length: parts }, (_, i) => base + (i < rem ? 1 : 0));
}

function sectionForStudentIndex1Based(i) {
  const counts = partitionStudentsAcrossSections(studentCount, sectionCount);
  let acc = 0;
  for (let s = 0; s < counts.length; s++) {
    acc += counts[s];
    if (i <= acc) return sectionNames[s];
  }
  return sectionNames[sectionNames.length - 1];
}

let md = `<!-- Generated by backend/scripts/generate-demo-seed-accounts.mjs — re-run after changing demo seed roster. -->

# Demo seed accounts

Credentials for the synthetic roster created when \`SEED_DEMO_DATA=true\` (see \`backend/src/seed/demoDataset.ts\`). **Every account below uses the same password.**

**Security:** Demo-only. Do not use for real users or production; treat this file as sensitive if the passwords matter in your environment.

| Email | Password | Display name | Section |
|-------|----------|--------------|---------|
| ${teacherEmail} | \`${pwd}\` | ${teacherName} | *(teacher — owns all sections)* |
`;

for (let i = 1; i <= studentCount; i++) {
  const n = String(i).padStart(3, "0");
  const email = `demo.seed.s${n}@secondteacher.dev`;
  const name = `Seed Student ${n}`;
  const section = sectionForStudentIndex1Based(i);
  md += `| ${email} | \`${pwd}\` | ${name} | ${section} |\n`;
}

let csv = "Email,Password,Display name,Section\n";
csv += `"${teacherEmail}","${pwd}","${teacherName}","teacher"\n`;
for (let i = 1; i <= studentCount; i++) {
  const n = String(i).padStart(3, "0");
  csv += `"demo.seed.s${n}@secondteacher.dev","${pwd}","Seed Student ${n}","${sectionForStudentIndex1Based(i)}"\n`;
}

const docsDir = path.join(repoRoot, "docs");
fs.mkdirSync(docsDir, { recursive: true });
fs.writeFileSync(path.join(docsDir, "demo-seed-accounts.md"), md);
fs.writeFileSync(path.join(docsDir, "demo-seed-accounts.csv"), csv);
console.log("Wrote docs/demo-seed-accounts.md and docs/demo-seed-accounts.csv");
